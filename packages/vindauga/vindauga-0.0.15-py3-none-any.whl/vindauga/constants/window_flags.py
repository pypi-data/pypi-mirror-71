# -*- coding: utf-8 -*-
# Window Flags masks

# Window can be moved.
# @see Window::flags.
wfMove = 0x01

# Window can be resized and has a grow icon in the lower-right corner.
# @see Window::flags.
wfGrow = 0x02

# Window frame has a close icon that can be mouse-clicked to close the
# window.
# @see Window::flags.
wfClose = 0x04

# Window frame has a zoom icon that can be mouse-clicked.
# @see Window::flags.
wfZoom = 0x08
