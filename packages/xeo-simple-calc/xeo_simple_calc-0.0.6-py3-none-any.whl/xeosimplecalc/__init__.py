def add(num1, num2):
    return num1 + num2
def subtract(num1, num2):
    return num1 - num2
def multiply(num1, num2):
    return num1 * num2
def divide(num1, num2):
    return num1 / num2
def exponentiation(num, degree):
    return num ** degree
def square_root(num):
    if num <= 0:
        return 0
    else:
        return num ** 0.5